class Solution:
    def fourSumCount(self, nums1, nums2, nums3, nums4) -> int:
        nums1=self.convert(nums1)
        nums2=self.convert(nums2)
        nums3=self.convert(nums3)
        nums4 = self.convert(nums4)


        # res=set()
        res=0
        for i in nums1.keys():
            t1=-i
            for j in nums2.keys():
                t2=t1-j
                for k in nums3.keys():
                    t3=t2-k
                    if nums4.get(t3):
                        res+=nums1[i]*nums2[j]*nums3[k]*nums4[t3]
                        # res.append(str(i) + str(j) + str(k) + str(t3))
                        # res.add(str(i)+str(j)+str(k)+str(t3))
        # return len(res)
        return res
    def convert(self,nums4):
        n4 = dict()
        for num in nums4:
            n4[num] = 1 if not n4.get(num) else n4[num] + 1
        return n4

